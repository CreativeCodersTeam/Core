namespace CreativeCoders.Core.Comparing;

/// <summary>
/// Specifies the direction in which a collection is sorted.
/// </summary>
public enum SortOrder
{
    /// <summary>
    /// Sorts in ascending order, from smallest to largest.
    /// </summary>
    Ascending,

    /// <summary>
    /// Sorts in descending order, from largest to smallest.
    /// </summary>
    Descending
}
